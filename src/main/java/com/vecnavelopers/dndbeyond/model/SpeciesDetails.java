package com.vecnavelopers.dndbeyond.model;

public class SpeciesDetails {
    private String speciesName;
    private int speciesSpeed;
    private String speciesSize;

    // Getters and Setters
    public String getSpeciesName() { return speciesName; }

    public void setSpeciesName(String speciesName) { this.speciesName = speciesName; }

    public int getSpeciesSpeed() { return speciesSpeed; }

    public void setSpeciesSpeed(int speciesSpeed) { this.speciesSpeed = speciesSpeed; }

    public String getSpeciesSize() { return speciesSize; }

    public void setSpeciesSize(String speciesSize) { this.speciesSize = speciesSize; }
}
